let current = 0;
let answers = Array(quizData.length).fill(null);

const letters = ["A", "B", "C", "D"];
const questionText = document.getElementById("questionText");
const lectureTag = document.getElementById("lectureTag");
const optionsBox = document.getElementById("options");
const feedback = document.getElementById("feedback");
const currentNo = document.getElementById("currentNo");
const totalNo = document.getElementById("totalNo");
const liveScore = document.getElementById("liveScore");
const progressBar = document.getElementById("progressBar");
const prevBtn = document.getElementById("prevBtn");
const nextBtn = document.getElementById("nextBtn");
const questionNav = document.getElementById("questionNav");
const resultModal = document.getElementById("resultModal");
const restartBtn = document.getElementById("restartBtn");

totalNo.textContent = quizData.length;

function getScore() {
  return answers.filter((ans, i) => ans === quizData[i].answer).length;
}

function renderQuestion() {
  const item = quizData[current];
  lectureTag.textContent = item.lecture;
  questionText.textContent = item.q;
  currentNo.textContent = current + 1;
  progressBar.style.width = `${((current + 1) / quizData.length) * 100}%`;
  optionsBox.innerHTML = "";
  feedback.textContent = "";
  feedback.className = "feedback";

  item.choices.forEach((choice, index) => {
    const btn = document.createElement("button");
    btn.className = "option";
    btn.innerHTML = `<span class="letter">${letters[index]}</span><span>${choice}</span>`;
    btn.onclick = () => selectAnswer(index);

    if (answers[current] !== null) {
      btn.classList.add("disabled");
      btn.disabled = true;
      if (index === item.answer) btn.classList.add("correct");
      if (index === answers[current] && index !== item.answer) btn.classList.add("wrong");
    }
    optionsBox.appendChild(btn);
  });

  if (answers[current] !== null) {
    const ok = answers[current] === item.answer;
    feedback.textContent = ok ? "Correct ✅" : `Incorrect ❌ Correct Answer: ${letters[item.answer]}. ${item.choices[item.answer]}`;
    feedback.classList.add(ok ? "good" : "bad");
  }

  prevBtn.disabled = current === 0;
  nextBtn.textContent = current === quizData.length - 1 ? "Finish" : "Next";
  updateScore();
  renderNav();
}

function selectAnswer(index) {
  if (answers[current] !== null) return;
  answers[current] = index;
  renderQuestion();
}

function updateScore() {
  liveScore.textContent = `${getScore()} / ${quizData.length}`;
}

function renderNav() {
  questionNav.innerHTML = "";
  quizData.forEach((_, i) => {
    const btn = document.createElement("button");
    btn.className = "nav-dot";
    btn.textContent = i + 1;
    if (i === current) btn.classList.add("active");
    if (answers[i] !== null && answers[i] === quizData[i].answer) btn.classList.add("answered");
    if (answers[i] !== null && answers[i] !== quizData[i].answer) btn.classList.add("wrong-dot");
    btn.onclick = () => { current = i; renderQuestion(); };
    questionNav.appendChild(btn);
  });
}

function showResult() {
  const correct = getScore();
  const attempted = answers.filter(a => a !== null).length;
  const incorrect = answers.filter((a, i) => a !== null && a !== quizData[i].answer).length;
  const percent = Math.round((correct / quizData.length) * 100);
  document.getElementById("resultTotal").textContent = quizData.length;
  document.getElementById("resultScore").textContent = correct;
  document.getElementById("resultPercent").textContent = percent + "%";
  document.getElementById("resultCorrect").textContent = correct;
  document.getElementById("resultIncorrect").textContent = incorrect + ` / Unanswered: ${quizData.length - attempted}`;
  resultModal.classList.remove("hidden");
}

prevBtn.onclick = () => { if (current > 0) { current--; renderQuestion(); } };
nextBtn.onclick = () => {
  if (current < quizData.length - 1) { current++; renderQuestion(); }
  else showResult();
};
restartBtn.onclick = () => {
  answers = Array(quizData.length).fill(null);
  current = 0;
  resultModal.classList.add("hidden");
  renderQuestion();
};

renderQuestion();
